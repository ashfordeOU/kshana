/// **The headline end-to-end recovery.** Propagate a truth LMO arc under `mars_gmm3` gravity,
/// generate noisy two-way range + Doppler against two tracking stations, perturb the initial state
/// at the km level, and recover with the reduced-dynamic SRIF. The recovered position must converge
/// to the truth at the metres-to-tens-of-metres level (far below the km-scale initial error), and
/// the factored covariance must stay positive-definite across the whole arc.
///
/// Achieved (recorded, not asserted-to-the-decimal): a converged position RMS at the **sub-metre**
/// level over a 3-orbit, 30 s-cadence arc — comfortably inside the tens-of-metres done-criterion.
/// The two-station geometry (a realistic DSN/ESTRACK multi-complex configuration) gives the
/// plane-of-sky diversity that lets range + Doppler triangulate the full 3-D state quickly; a
/// single station also recovers but converges over more orbits (the line of sight must rotate
/// through the dynamics first).
#[test]
fn mars_lmo_reduced_dynamic_od_recovers_from_radiometric_data() {
    let epoch = 2_459_580.5;
    let nmax = 4;
    let fm = MarsForceModel::gmm3(nmax, epoch);
    let (r0, v0) = lmo_state();

    // ~3 orbits at a 30 s cadence (period ≈ 7100 s ⇒ ~3 orbits in ~21 300 s).
    let period =
        std::f64::consts::TAU * ((Body::mars().re + 400.0e3).powi(3) / Body::mars().mu).sqrt();
    let arc = 3.0 * period;
    let cadence = 30.0;
    let n = (arc / cadence) as usize;
    let times: Vec<f64> = (1..=n).map(|k| k as f64 * cadence).collect();

    let truth = truth_states(&fm, r0, v0, &times);
    let stas = stations();

    // DSN-class noise: range σ = 1 m, Doppler σ = 0.1 mm/s.
    let range_sigma = 1.0;
    let doppler_sigma = 1.0e-4;
    let obs = radiometric_track(&truth, &times, &stas, range_sigma, doppler_sigma);

    // Perturb the initial state at the km / m·s⁻¹ level (the realistic a-priori-knowledge error).
    let r0_guess = [r0[0] + 2.0e3, r0[1] - 1.5e3, r0[2] + 1.0e3]; // ~2.7 km off
    let v0_guess = [v0[0] + 2.0, v0[1] - 1.5, v0[2] + 1.0]; // ~2.7 m/s off
    let init_pos_err = norm([
        r0_guess[0] - r0[0],
        r0_guess[1] - r0[1],
        r0_guess[2] - r0[2],
    ]);

    // Lightly reduced-dynamic: a small empirical-process-noise floor (the tier is exercised, but
    // the gravity-only arc has no model error for it to chase, so a low tightness is the correct
    // regime — see `reduced_dynamic_tuning_improves_the_lmo_fit` for the model-error case).
    let cfg = lmo_config(0.1);
    let run = ReducedDynamicOd::new(fm.clone(), cfg)
        .run_radiometric(r0_guess, v0_guess, &obs)
        .expect("radiometric OD run");

    // (1) Covariance stayed positive-definite throughout (the SRIF guarantee).
    assert!(
        run.covariance_pd_throughout,
        "factored covariance lost positive-definiteness during the arc"
    );
    assert!(
        is_spd(&run.final_cov),
        "final covariance is not symmetric positive-definite"
    );

    // (2) The recovered position converged to the truth at the m–tens-of-m level — far below the
    //     ~2.7 km initial perturbation. The achieved RMS is printed for the validation record.
    let rms = converged_pos_rms(&run.steps, &truth);
    println!(
        "Mars-LMO reduced-dynamic OD: converged position RMS = {rms:.3} m \
         (initial perturbation {init_pos_err:.0} m, {} epochs, {nmax}×{nmax} field)",
        run.steps.len()
    );
    assert!(
        rms < 50.0,
        "converged position RMS {rms:.3} m exceeds the tens-of-metres done-criterion \
         (initial error {init_pos_err:.0} m)"
    );
    // It is a real recovery, not a no-op: the converged error is far smaller than the initial one.
    assert!(
        rms < init_pos_err * 1.0e-2,
        "filter did not materially improve on the initial guess: RMS {rms:.3} m vs init \
         {init_pos_err:.0} m"
    );
}

/// **Cruise→LMO tuning continuum on the LMO arc.** Adding Mars drag to the *truth* but not to the
/// filter template injects an unmodelled along-track acceleration the reduced-dynamic empirical
/// tier must absorb. Sweeping the `dynamic_tightness` knob upward (more empirical freedom) must
/// improve the recovered fit — the same cruise↔LMO continuum the D2.2 unit tests show, exercised
/// here end-to-end on the Mars-LMO radiometric arc with a real mismodelling.
#[test]
fn reduced_dynamic_tuning_improves_the_lmo_fit() {
    let epoch = 2_459_580.5;
    let nmax = 4;
    // Truth carries Mars drag at a low-LMO ballistic coefficient; the filter template does NOT.
    let cd_am = 0.02; // m²/kg
                      // A lower (denser-atmosphere) LMO so the drag mismodelling is a real signal.
    let mars = Body::mars();
    let r_orbit = mars.re + 200.0e3;
    let vc = (mars.mu / r_orbit).sqrt();
    let inc = 50.0_f64.to_radians();
    let r0 = [r_orbit, 0.0, 0.0];
    let v0 = [0.0, vc * inc.cos(), vc * inc.sin()];

    let truth_fm = MarsForceModel::gmm3(nmax, epoch).with_drag(cd_am);
    let filter_fm = MarsForceModel::gmm3(nmax, epoch); // drag-blind template

    let period = std::f64::consts::TAU * (r_orbit.powi(3) / mars.mu).sqrt();
    let arc = 2.0 * period;
    let cadence = 20.0;
    let n = (arc / cadence) as usize;
    let times: Vec<f64> = (1..=n).map(|k| k as f64 * cadence).collect();

    let truth = truth_states(&truth_fm, r0, v0, &times);
    let stas = stations();
    let obs = radiometric_track(&truth, &times, &stas, 1.0, 1.0e-4);

    // Same km-level initial perturbation for every tightness.
    let r0_guess = [r0[0] + 1.5e3, r0[1] - 1.0e3, r0[2] + 0.8e3];
    let v0_guess = [v0[0] + 1.5, v0[1] - 1.0, v0[2] + 0.8];

    // Near-dynamic (the empirical tier is held near zero ⇒ cannot absorb the drag) vs
    // reduced-dynamic (the empirical tier slews to absorb the unmodelled along-track drag).
    let dynamic = ReducedDynamicOd::new(filter_fm.clone(), lmo_config(0.0))
        .run_radiometric(r0_guess, v0_guess, &obs)
        .expect("dynamic run");
    let reduced = ReducedDynamicOd::new(filter_fm.clone(), lmo_config(1.0))
        .run_radiometric(r0_guess, v0_guess, &obs)
        .expect("reduced-dynamic run");

    let rms_dyn = converged_pos_rms(&dynamic.steps, &truth);
    let rms_red = converged_pos_rms(&reduced.steps, &truth);
    println!(
        "cruise→LMO continuum: dynamic RMS = {rms_dyn:.3} m, reduced-dynamic RMS = {rms_red:.3} m \
         (truth has unmodelled Mars drag the filter template lacks)"
    );

    // Both runs stay covariance-PD.
    assert!(
        dynamic.covariance_pd_throughout && reduced.covariance_pd_throughout,
        "covariance positivity lost in the tuning-continuum runs"
    );
    // The reduced-dynamic knob improves the fit on the drag-mismodelled LMO arc — the continuum is
    // real, not a switch: the empirical tier absorbing the drag lowers the recovered RMS.
    assert!(
        rms_red < rms_dyn,
        "reduced-dynamic tuning did not improve the LMO fit: reduced {rms_red:.3} m \
         not < dynamic {rms_dyn:.3} m"
    );
}
