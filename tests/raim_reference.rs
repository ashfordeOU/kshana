//! RAIM / ARAIM **statistical-kernel** reference test (external oracle: SciPy).
//!
//! kshana's snapshot-RAIM / solution-separation / ARAIM stack builds its detection
//! thresholds and protection-level multipliers from a dependency-free regularized
//! incomplete-gamma function (`chi2_cdf`/`chi2_quantile`, `normal_cdf`/`normal_quantile`,
//! `noncentral_chi2_cdf`) plus a bisection for the non-centrality `pbias`. This test pins
//! every one of those primitives against **SciPy** (`scipy.stats.chi2`, `.norm`, `.ncx2`
//! and `scipy.optimize.brentq`) — a wholly independent implementation (Cephes/Boost under
//! the hood, a different algorithm from Kshana's Numerical-Recipes series/continued-
//! fraction), evaluated across the false-alarm, missed-detection and redundancy ranges
//! the RAIM stack actually uses. Reproducing SciPy's chi-squared / non-central / normal
//! laws to the tolerances below makes the integrity kernel **externally validated**, not
//! merely self-consistent.
//!
//! Oracle: SciPy 1.17.0 (NumPy 2.4.1). Reference values are the SciPy outputs to full f64.
//! The geometry that wraps this kernel (the `(GᵀG)⁻¹` slope/covariance projection) is
//! validated separately against gnss_lib_py (DOP) and on real IGS SP3 geometry; the
//! ARAIM MHSS *integrity-risk budget allocation* has no published numeric oracle and is
//! tracked honestly as founder-gated in `docs/ARAIM_REFERENCE.md` — not asserted here.

use kshana::raim::{
    chi2_cdf, chi2_quantile, noncentral_chi2_cdf, normal_cdf, normal_quantile, pbias,
};

/// Pass when `got` is within `abstol + reltol·|want|` of `want` (a combined absolute /
/// relative band, so both tail probabilities near 0 and O(10) quantiles are checked
/// meaningfully).
fn close(got: f64, want: f64, abstol: f64, reltol: f64, what: &str) {
    let d = (got - want).abs();
    assert!(
        d <= abstol + reltol * want.abs(),
        "{what}: kshana {got:.12e} vs SciPy {want:.12e} (|Δ|={d:.2e} > {abstol:.0e}+{reltol:.0e}·|want|)"
    );
}

